                        <h3>Punta Cana</h3>

                              <p>Es una localidad situada al este de la República Dominicana, en la provincia de La Altagracia. En esta localidad se ubican varios complejos hoteleros, cuya superficie total es de unos 420 000 m² (equivalentes a 42 hectáreas o 0,42 km²).</p>
                                    <p>En Punta Cana se encuentra el Aeropuerto Internacional Punta Cana (PUJ), el principal aeropuerto del país, situado a unos 30 km hacia el interior, en la carretera que lleva desde Higüey hasta La Romana. Este aeropuerto recibe el 64% de todos los vuelos que llegan al país, por lo que recibe más pasajeros que el Aeropuerto Internacional de Las Américas, situado en Santo Domingo.</p>
<p>Administrativamente, Punta Cana es un distrito municipal perteneciente al municipio de Higüey bajo el nombre de Distrito Municipal Turístico Verón Punta Cana. Este distrito tenía según el censo de 2010 una población de 43 982 habitantes, de los cuales 37 286 eran del área urbana y 6696 del área rural.</p>
<p>La zona de Punta Cana se inicia al norte con la Playa de Arena Gorda, siguiendo después la famosa Playa Bávaro, Playa Uvero Alto, Playa Macao y la Playa de El Cortecito. Cuando se inicia la vuelta de la punta se encuentran las playas de Cabeza de Toro, Cabo Engaño, Punta Cana y Juanillo. El litoral de la zona de Punta Cana se extiende 50 km de costa. La parte de playa más extensa es la de Bávaro, considerado por el gobierno de República Dominicana como tesoro nacional por la riqueza de su flora y fauna y por las bellezas naturales que allí se aprecian.</p>
  <p>Las playas son de arena blanca y fina, y el mar de un suave color azul verdoso, sin alcanzar el turquesa de las playas caribeñassituadas más al sur. El mar presenta generalmente oleaje y la playa se hunde muy rápidamente en el mar. El agua siempre es transparente y abundan las algas en algunas áreas.</p>
                          
