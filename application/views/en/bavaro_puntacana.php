                     	<h3>Punta Cana</h3>

                              <p>The Punta Cana area has an estimated population of 100,000, with an annual growth rate of 6%.[3] To the north, it borders the village and beach of Cabeza de Toro, and the Bávaro and El Cortecito beaches. The nearest city, the 500-year-old Higüey, is 45 kilometres (28 mi) away, which takes about an hour to reach by car. European entrepreneurs, particularly Spanish hotel chains, own all but two of the over 50 megaresorts at the Punta Cana tourism destination.</p>
                           			<p>The province’s 100-kilometre (62 mi) coastline tends to be mildly windy. The ocean waters are mainly shallows, with several natural marine pools in which visitors can bathe without danger. From north to south, the main beaches are Uvero Alto, Macao, Arena Gorda, Bávaro, El Cortecito, Las Corales and Cabeza de Toro, all north of the cape; and Cabo Engaño, Punta Cana and Juanillo south of the cape.</p>
<p>Bávaro is the area starting from Cabeza de Toro until Macao Beach. As the hotels started to rise along the East coast, Bavaro itself became a center of services with shopping malls, fast-food stores, drug stores, fine restaurants, banks, clinics, workshops, supermarkets, and schools. The major town in the district is Veron, now bigger than Higüey in territory, a spontaneous – and poor – urban development running along the original road from the west. Verón, last name of the French proprietor of a timberline business in the early 1930s, is now the base-city for hotel workers and related. It has, besides Bávaro, one of the only four gas stations in Punta Cana. The very next is located 48 kilometres (30 mi) west in Higüey, at the Fruisa crossroads, with a new Texaco gas station opened April 2010, 3 kilometres (1.9 mi) south of Macao beach, and the new Shell gas station close to the airport (on the highway Coral) opened at the end of 2010.</p>
  
                          
