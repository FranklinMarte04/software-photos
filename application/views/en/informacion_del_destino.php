					<h2>Republica Dominicana</h2>
					<p>Es un país que ocupa algo más de los dos tercios orientales de La Española, en el archipiélago de las Antillas Mayores. El tercio occidental de la isla está ocupado por Haití. Así pues, La Española es una isla compartida por dos países. Tanto por superficie como por población, la República Dominicana es el segundo país mayor del Caribe (después de Cuba). Su extensión territorial es de 48 311 kilómetros cuadrados y su población total es de 9 445 281 habitantes según el censo de 2010.4 Limita al norte con el océano Atlántico, al sur con el mar Caribe o mar de las Antillas, al este con el Canal de la Mona, que la separa de Puerto Rico y al oeste con la República de Haití.</p>
                    <p>Habitado por taínos desde el siglo VII, el territorio del país fue descubierto por Cristóbal Colón en 1492 convirtiéndose en el lugar del primerasentamiento europeo en América, nombrado como Santo Domingo, actual capital del país y primera capital de España en el Nuevo Mundo. Después de tres siglos de dominación española, con interludios franceses y haitianos, el país alcanzó la primera independencia en 1821pero fue tomado rápidamente por Haití en 1822.</p>
                    
                                         
                     <blockquote class="styled">
                     	      <p>El progreso económico del país se ejemplifica con su sistema avanzado de telecomunicaciones.12 La migración internacional afecta en gran medida al país, ya que recibe y envía gran flujo de migrantes. La inmigración irregular de haitianos y la integración en materia legal de los descendientes de estos es el principal problema inmigratorio; la población total de origen haitiano se estima en alrededor de 800 000.13 En los Estados Unidos existe una gran diáspora dominicana, contabilizada en 1,3 millones de personas;14 esa diáspora ayuda al desarrollo nacional, enviando miles de millones de dólares a sus familias, lo que representa una décima parte del PIB</p>
                           		<p>La República Dominicana tiene la novena economía más grande de América Latina y la segunda de Centroamérica y el Caribe.11 Aunque conocida por la producción de azúcar, la economía está ahora dominada por los servicios. No obstante, el desempleo,1 la corrupción gubernamental y el servicio eléctrico siguen siendo problemas importantes para el país. También tiene una marcada desigualdad de ingresos</p>	
                        <p>Tras la victoria obtenida en la Guerra de la Independencia Dominicana en 1844, los dominicanos experimentaron varias luchas, en su mayoría internas, y también un breve regreso de la dominación española (1861-1865). La ocupación estadounidense de 1916 a 1924 y, posteriormente, los seis años en calma y prosperidad de Horacio Vásquez (1924-1930) fueron seguidos por la dictadura de Rafael Leónidas Trujillo hasta 1961. La guerra civil de 1965 terminó con una intervención liderada por Estados Unidos y fue seguida por varios períodos de gobiernos represivos de Joaquín Balaguer (1966-1978). Desde entonces, la República Dominicana se ha movido hacia una democracia representativa.</p>

